function readJson(key, fallback) {
  try {
    return JSON.parse(localStorage.getItem(key)) ?? fallback;
  } catch {
    return fallback;
  }
}

function writeJson(key, value) {
  localStorage.setItem(key, JSON.stringify(value));
}

function slug(value) {
  return value.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/(^-|-$)/g, "");
}

function flattenStrings(value) {
  if (Array.isArray(value)) return value.flatMap(flattenStrings);
  if (value && typeof value === "object") return Object.values(value).flatMap(flattenStrings);
  return typeof value === "string" ? [value] : [];
}

function matchesPhase(phase, query) {
  if (!query) return true;
  return flattenStrings(phase).join(" ").toLowerCase().includes(query.toLowerCase());
}

function pagePill(pages) {
  return `<span class="page-pill">p. ${pages}</span>`;
}

function renderList(items, className = "") {
  return `<ul class="detail-list ${className}">${items.map((item) => `<li>${item}</li>`).join("")}</ul>`;
}

function renderDetailSection(title, items, className = "") {
  return `
    <section class="info-card detail-section">
      <h4>${title}</h4>
      ${renderList(items, className)}
    </section>
  `;
}

function renderPhaseList() {
  const visiblePhases = phases.filter((phase) => matchesPhase(phase, state.query));
  phaseList.innerHTML = "";

  if (!visiblePhases.length) {
    phaseList.innerHTML = `<p class="empty-state">No phase matches that search.</p>`;
    return;
  }

  if (!visiblePhases.some((phase) => phase.id === state.activePhaseId)) {
    state.activePhaseId = visiblePhases[0].id;
  }

  visiblePhases.forEach((phase, index) => {
    const button = document.createElement("button");
    button.className = `phase-button ${phase.id === state.activePhaseId ? "is-active" : ""}`;
    button.type = "button";
    button.setAttribute("aria-pressed", phase.id === state.activePhaseId ? "true" : "false");
    button.innerHTML = `
      <span class="phase-number">${index + 1}</span>
      <span>
        <span class="phase-button-title">${phase.nav}: ${phase.title}</span>
        <span class="phase-button-meta">${phase.time}</span>
      </span>
      ${pagePill(phase.pages)}
    `;
    button.addEventListener("click", () => {
      state.activePhaseId = phase.id;
      render();
    });
    phaseList.appendChild(button);
  });
}

function renderPhaseDetail() {
  const phase = phases.find((item) => item.id === state.activePhaseId) ?? phases[0];
  const noteId = `note-${phase.id}`;
  const templateLinks = phase.templates
    .map((templateName) => templates.find((template) => template.title === templateName))
    .filter(Boolean)
    .map((template) => `<a class="mini-link" href="${template.href}">${template.title}</a>`)
    .join("");

  const checklist = phase.checkpoints
    .map((item) => {
      const id = `${phase.id}-${slug(item)}`;
      const checked = progress[id] ? "checked" : "";
      return `
        <li>
          <label>
            <input type="checkbox" data-progress-id="${id}" ${checked}>
            <span>${item}</span>
          </label>
        </li>
      `;
    })
    .join("");

  phaseDetail.innerHTML = `
    <div class="detail-banner">
      <div class="detail-topline">
        ${pagePill(phase.pages)}
        <span class="tag">${phase.time}</span>
      </div>
      <h3>${phase.title}</h3>
      <p class="detail-summary">${phase.summary}</p>
    </div>
    <div class="detail-body">
      <section class="info-card detail-goal">
        <h4>Finish Line For This Stop</h4>
        <p>${phase.goal}</p>
      </section>
      <div class="info-grid">
        ${renderDetailSection("Quick Start", phase.quickStart)}
        <section class="info-card">
          <h4>Checklist</h4>
          <ul class="checklist">${checklist}</ul>
        </section>
      </div>
      ${renderDetailSection("Do The Work", phase.studentMoves)}
      <div class="info-grid">
        ${renderDetailSection("Evidence To Save", phase.evidence)}
        ${renderDetailSection("Ask Or Confirm", phase.questions, "question-list")}
      </div>
      <div class="info-grid">
        ${renderDetailSection("Mastery Looks Like", phase.masteryChecks, "mastery-list")}
        ${renderDetailSection("Common Mistakes", phase.avoid, "avoid-list")}
      </div>
      ${renderDetailSection("Adult Roles", phase.adultRoles)}
      <section class="info-card detail-section">
        <h4>Booklet Connection</h4>
        <p>${phase.bookletConnection}</p>
      </section>
      <section class="info-card">
        <h4>Related Files</h4>
        <div class="inline-links">${templateLinks || "<p>No related template files yet.</p>"}</div>
      </section>
      <section class="note-panel">
        <h4>Draft Notes</h4>
        <textarea id="${noteId}" data-note-id="${phase.id}" placeholder="Questions, mentor feedback, next actions">${notes[phase.id] ?? ""}</textarea>
        <div class="save-row">
          <button class="small-button" type="button" data-save-note="${phase.id}">Save Note</button>
          <span class="saved-state" id="saved-${phase.id}">Saved on this device</span>
        </div>
      </section>
    </div>
  `;

  phaseDetail.querySelectorAll("[data-progress-id]").forEach((checkbox) => {
    checkbox.addEventListener("change", (event) => {
      progress[event.target.dataset.progressId] = event.target.checked;
      writeJson(progressKey, progress);
    });
  });

  phaseDetail.querySelector("[data-save-note]").addEventListener("click", (event) => {
    const phaseId = event.target.dataset.saveNote;
    const textarea = phaseDetail.querySelector(`[data-note-id="${phaseId}"]`);
    notes[phaseId] = textarea.value;
    writeJson(notesKey, notes);
    const saved = phaseDetail.querySelector(`#saved-${phaseId}`);
    saved.textContent = "Saved just now";
    window.setTimeout(() => {
      saved.textContent = "Saved on this device";
    }, 1600);
  });
}

