function renderPhaseList() {
  const visiblePhases = phases.filter((phase) => matchesPhase(phase, state.query));
  phaseList.innerHTML = "";

  if (!visiblePhases.length) {
    phaseList.innerHTML = `<p class="empty-state">No phase matches that search.</p>`;
    return;
  }

  if (!visiblePhases.some((phase) => phase.id === state.activePhaseId)) {
    state.activePhaseId = visiblePhases[0].id;
  }

  visiblePhases.forEach((phase, index) => {
    const button = document.createElement("button");
    button.className = `phase-button ${phase.id === state.activePhaseId ? "is-active" : ""}`;
    button.type = "button";
    button.setAttribute("aria-pressed", phase.id === state.activePhaseId ? "true" : "false");
    button.innerHTML = `
      <span class="phase-number">${index + 1}</span>
      <span>
        ${phaseButtonTitleHtml(phase)}
      </span>
      ${phaseButtonPagePillHtml(phase)}
    `;
    button.addEventListener("click", () => {
      state.activePhaseId = phase.id;
      render();
    });
    phaseList.appendChild(button);
  });
}

