function readJson(key, fallback) {
  try {
    return JSON.parse(localStorage.getItem(key)) ?? fallback;
  } catch {
    return fallback;
  }
}

function writeJson(key, value) {
  localStorage.setItem(key, JSON.stringify(value));
}

function slug(value) {
  return value.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/(^-|-$)/g, "");
}

function flattenStrings(value) {
  if (Array.isArray(value)) return value.flatMap(flattenStrings);
  if (value && typeof value === "object") return Object.values(value).flatMap(flattenStrings);
  return typeof value === "string" ? [value] : [];
}

function matchesPhase(phase, query) {
  if (!query) return true;
  return flattenStrings(phase).join(" ").toLowerCase().includes(query.toLowerCase());
}

function pagePill(pages) {
  return `<span class="page-pill">p. ${pages}</span>`;
}

function renderList(items, className = "") {
  return `<ul class="detail-list ${className}">${items.map((item) => `<li>${item}</li>`).join("")}</ul>`;
}

function renderDetailSection(title, items, className = "") {
  return `
    <section class="info-card detail-section">
      <h4>${title}</h4>
      ${renderList(items, className)}
    </section>
  `;
}

function renderPhaseList() {
  const visiblePhases = phases.filter((phase) => matchesPhase(phase, state.query));
  phaseList.innerHTML = "";

  if (!visiblePhases.length) {
    phaseList.innerHTML = `<p class="empty-state">No phase matches that search.</p>`;
    return;
  }

  if (!visiblePhases.some((phase) => phase.id === state.activePhaseId)) {
    state.activePhaseId = visiblePhases[0].id;
  }

  visiblePhases.forEach((phase, index) => {
    const button = document.createElement("button");
    button.className = `phase-button ${phase.id === state.activePhaseId ? "is-active" : ""}`;
    button.type = "button";
    button.setAttribute("aria-pressed", phase.id === state.activePhaseId ? "true" : "false");
    button.innerHTML = `
      <span class="phase-number">${index + 1}</span>
      <span>
        <span class="phase-button-title">${phase.nav}: ${phase.title}</span>
        <span class="phase-button-meta">${phase.time}</span>
      </span>
      ${pagePill(phase.pages)}
    `;
    button.addEventListener("click", () => {
      state.activePhaseId = phase.id;
      render();
    });
    phaseList.appendChild(button);
  });
}

